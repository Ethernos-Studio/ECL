#[derive(Debug, Clone)]
pub enum Token {
    Print,
    Println,
    Var,
    For,
    In,
    If,
    Else,
    LeftParen,
    RightParen,
    LeftBrace,
    RightBrace,
    Semicolon,
    Comma,
    Colon,
    Equal,
    Plus,
    Minus,
    Multiply,
    Divide,
    Less,
    Greater,
    Range,  // .. 运算符
    Identifier(String),
    Number(f64),
    String(String),
    Error(String),
    Eof,
}