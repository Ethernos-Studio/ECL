#[derive(Debug, Clone)]
pub enum Token {
    Print,
    Println,
    Var,
    For,
    In,
    If,
    Else,
    Func,
    Expr,
    Return,
    True,
    False,
    LeftParen,
    RightParen,
    LeftBrace,
    RightBrace,
    Semicolon,
    Comma,
    Colon,
    Equal,
    Plus,
    Minus,
    Multiply,
    Divide,
    Less,
    Greater,
    Range,  // .. 运算符
    LessThan,
    GreaterThan,
    // 类型关键字
    Int,
    Str,
    Bool,
    Float,
    Double,
    Identifier(String),
    Number(f64),
    String(String),
    Error(String),
    Eof,
}